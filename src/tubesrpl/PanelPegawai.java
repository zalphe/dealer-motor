    public void refreshCreateAccount() {
        ResultSet rs;
        String query, kode, temp = null;
        int counter = 0;
        if (cbjenisuser.getSelectedItem().toString().equals("Manager")) {
            query = "select * from tb_pegawai where posisi='manager'";
            kode = "MGR";
        } else if (cbjenisuser.getSelectedItem().toString().equals("Sales")) {
            query = "select * from tb_pegawai where posisi='sales'";
            kode = "SLS";
        } else {
            query = "select * from tb_pegawai where posisi='warehouse'";
            kode = "WHK";
        }

        try {
            rs = conn.getData(query);
            while (rs.next()) {
                temp = rs.getString("id_pegawai");
            }
            if (temp != null) {
                counter = Integer.parseInt(temp.substring(4, 8));
                String x = String.format("%04d", ++counter);
                txtidpeg.setText(kode + "-" + x);
            } else {
                txtidpeg.setText(kode + "-0001");
            }
        } catch (SQLException ex) {

        }
    }

