    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:

        if (btnedit.getText().equals("EDIT")) {
            String input = JOptionPane.showInputDialog(null, "ID Pegawai yang akan diedit :  ", "Edit Data Pegawai", JOptionPane.QUESTION_MESSAGE);
            if (cekIdPegawai(input)) {
                rs = null;
                reset();
                rs = conn.getData("select * from tb_pegawai where id_pegawai like '" + input + "'");
                try {
                    while (rs.next()) {
                        //cbjenisuser.setSelectedItem(rs.getString("posisi"));
                        cbjenisuser.setEnabled(false);
                        txtidpeg.setText(rs.getString("id_pegawai"));
                        txtnama.setText(rs.getString("nama_pegawai"));
                        txtalamat.setText(rs.getString("alamat"));
                        txtnotelp.setText(rs.getString("no_telp"));
                        txtemail.setText(rs.getString("email"));
                        int i = rs.getString("tgl_lahir").length();
                        cbtgl.setSelectedItem((rs.getString("tgl_lahir")).substring(0, 2));
                        cbbln.setSelectedItem((rs.getString("tgl_lahir")).substring(2, (i - 5)));
                        cbthn.setSelectedItem((rs.getString("tgl_lahir")).substring((i - 4), i));
                        btnedit.setText("SAVE UPDATE");
                        btncreate.setEnabled(false);
                        btndelete.setEnabled(false);
                        lblpass.setVisible(false);
                        txtpass.setVisible(false);
                        lblrepass.setVisible(false);
                        txtconpass.setVisible(false);
                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error " + e.getMessage(), "Warning", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "ID " + input + " tidak dapat ditemukan", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            String tgl = cbtgl.getSelectedItem().toString() + " " + cbbln.getSelectedItem().toString() + " "
                    + cbthn.getSelectedItem().toString();
            String query = " update tb_pegawai set nama_pegawai='" + txtnama.getText() + "', alamat='" + txtalamat.getText() + "', no_telp='"
                    + txtnotelp.getText() + "', email='" + txtemail.getText() + "', tgl_lahir='" + tgl + "' where id_pegawai='" + txtidpeg.getText() + "'";
            conn.runQuery(query);
            reset();
            JOptionPane.showMessageDialog(null, "Biodata sudah berhasil diperbaharui. Terima kasih.", "Information", JOptionPane.INFORMATION_MESSAGE);
            btnedit.setText("EDIT");
            btncreate.setEnabled(true);
            btndelete.setEnabled(true);
            reset();
        }
    }//GEN-LAST:event_btneditActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        try {
            String input = JOptionPane.showInputDialog(null, "ID Pegawai yang akan dihapus :  ", "Hapus Data Pegawai", JOptionPane.QUESTION_MESSAGE);

            if (cekIdPegawai(input)) {
                int in = JOptionPane.showConfirmDialog(null, "Data pegawai atas ID " + input + " akan dihapus. Lanjutkan menghapus data ?", "Pesan Konfirmasi", JOptionPane.OK_CANCEL_OPTION);
                if (in == 0) {
                    String query = "delete from tb_pegawai where id_pegawai='" + input + "'";
                    conn.runQuery(query);
                    reset();
                    JOptionPane.showMessageDialog(null, "Data pegawai atas ID " + txtidpeg.getText() + " berhasil dihapus", "Information", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Data pegawai atas ID " + input + " tidak dapat ditemukan", "Warning", JOptionPane.WARNING_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error " + e.getMessage(), "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btndeleteActionPerformed

