        // TODO add your handling code here:
        String tgl = cbtgl.getSelectedItem().toString() + " " + cbbln.getSelectedItem().toString() + " "
                + cbthn.getSelectedItem().toString();
        String query = " update tb_pegawai set nama='" + txtnama.getText() + "', alamat='" + txtalamat.getText() + "', no_telp='"
                + txtnotelp.getText() + "', email='" + txtemail.getText() + "', tgl_lahir='" + tgl + "' where id_pegawai='" + pg.getIdPegawai() + "'";
        conn.runQuery(query);
        clearForm();
