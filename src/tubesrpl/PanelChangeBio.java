    public void setIsi() {
        txtnama.setText(pg.getNama());
        txtalamat.setText(pg.getAlamat());
        txtnotelp.setText(pg.getNoTelp());
        txtemail.setText(pg.getEmail());
        int i = pg.getTglLahir().length();
        cbtgl.setSelectedItem((pg.getTglLahir()).substring(0, 2));
        cbbln.setSelectedItem((pg.getTglLahir()).substring(3, i - 5));
        cbthn.setSelectedItem((pg.getTglLahir()).substring(i - 4, i));
    }

    public void clearForm(){
        txtnama.setText(null);
        txtalamat.setText(null);
        txtnotelp.setText(null);
        txtemail.setText(null);
        cbtgl.setSelectedIndex(0);
        cbbln.setSelectedIndex(0);
        cbthn.setSelectedIndex(0);
    }
